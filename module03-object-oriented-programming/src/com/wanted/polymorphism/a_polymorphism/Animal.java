package com.wanted.polymorphism.a_polymorphism;

public class Animal {

    public void eat(){
        System.out.println("동물이 밥을 먹습니다!!");
        System.out.println("우물우물우물");
    }

    public void run() {
        System.out.println("동물이 아주 빠르게 달립니다!!");
    }

    public void bark() {
        System.out.println("동물이 울어 째낍니다!");
    }

}
